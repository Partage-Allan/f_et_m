                    <p class="texte_presentation">
                        Passionnée depuis de longues années par le travail des matières, et la décoration, mais ayant  eu un parcours professionnel qui ne s’était pas fait dans cette voie,
                        j’ai fais le choix en 2009, de suivre une formation en Sellerie Nautique aux Ateliers de l’Enfer à Douarnenez.
                        Après obtention du CQP des Industries Nautiques et du CAP de Sellerie générale, je crée mon entreprise en Janvier 2013 où j’y travaille seule.<br/><br/>

                        Comme son nom l’indique, la sellerie consiste à créer ou à restaurer tous les objets permettant de s’asseoir. Dès lors, l’éventail de l’activité d’un sellier
                        garnisseur est très large.<br/>
                        Le travail de restauration peut s’effectuer sur des fauteuils, des chaises, des coussins,  des banquettes….. toutes ces assises pouvant provenir de différents milieux comme
                        le nautisme (carré de bateau), l’ameublement (salon ou chambre), l’automobile (selles de moto ou coussins de camping cars).<br/><br/>
                        Une place importante est également laissé aux travaux  ˮsur-mesure ˮ. Une conception particulière, une idée ou un projet proposé par un client 
                        peuvent m’amener à concevoir des objets qui seront des pièces uniques.  
                        Les matériaux utilisés sont aussi très spécifiques à la sellerie. Je vais travailler la mousse en collage ou en sculpture, les tissus, le cuir ou le simili cuir, la bâche…
                        Par ailleurs, des emprunts au métier de tapissier, maroquinier ou menuisier sont parfois nécessaires pour effectuer certains travaux.
                    </p>
